def sum_print(a,b):
    result = a + b
    print(result)
#This function prints a sum

def sum_return(a, b):
    result = a + b
    return result
#This function returns a sum

sum_print(4,3)
#This function prints 7

sum_return(4, 3)
#This adds the two variable values and then returns

result = sum_return(4,3)
#Assigns the sum to a variable

print(result)
#Prints the assigned variable of the sum in this case 'result'





